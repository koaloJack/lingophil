<h1>Trennbare und untrennbare Verben</h1>

<h2>Trennbare Verben</h2>

<p>Trennbare Verben werden mit Verbzusätzen – meist Präpositionen- zusammengesetzt,
   deren Sinn bekannt oder leicht verständlich ist -  zum Beispiel:
</p>

<p class="explainbox">ab-, an-, auf-, aus-, bei-, ein-, fest-, her-, hin-, los-,
mit-, nach-, um-, vor-, weg-, wieder-, zu-, zurück-,
zusammen-, (u.a.)
</p>


<p>Sie werden beim Sprechen betont.</p>

<p>Beispiel: </br>
<span class="bold">zu</span>machen  </br>
Sie <span class="bold">macht</span> das Fenster <span class="bold">zu</span>.  </br>
<span class="bold">auf</span>hängen  </br>
Er <span class="bold">hängt</span> die Bilder <span class="bold">auf</span>.  </br>
<span class="bold">aus</span>packen  </br>
Sie <span class="bold">packt</span> die Geschenke <span class="bold">aus</span>.  </br>
</p>


<h2>Untrennbare Verben</h2>

<p>Untrennbare Verben werden mit kurzen Vorsilben zusammengesetzt, deren Sinn kaum noch verständlich ist – zum Beispiel:
</p>

<p class="explainbox">be-, emp-, ent-, er-, ge-, miss-, ver-, zer-, (u.a.)
</p>

<p>Sie werden beim Sprechen nicht betont.</p>

<p>Beispiel: </br>

<span class="bold">be</span>suchen </br>
Ich <span class="bold">be</span>suche meinen Onkel. </br>
<span class="bold">ge</span>fallen </br>
Seine Freundin <span class="bold">ge</span>fällt mir gut. </br>
<span class="bold">er</span>zählen </br>
Sie <span class="bold">er</span>zählt ein Märchen. </br>
<span class="bold">ver</span>stehen </br>
Wir <span class="bold">ver</span>stehen den Text. </br>

</p>
